#include "libft.h"

static int	check_set(char c, char const *set)
{
	size_t	i;

	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	start;
	size_t	end;
	char	*arr;

	start = 0;
	while (s1[start] && check_set(s1[start], set))
		start++;
	end = ft_strlen(s1);
	while (end > start && check_set(s1[end - 1], set))
		end--;
	arr = (char *)malloc(sizeof(*s1) * (end - start + 1));
	if (!(arr))
		return (0);
	i = 0;
	while (start < end)
		arr[i++] = s1[start++];
	arr[i] = 0;
	return (arr);
}
